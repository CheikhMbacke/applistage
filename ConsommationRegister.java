    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
             msg.setText("");
             String cpt; 
             String dateDebut=debut.getText();
             String dateFin=fin.getText();
             String AI= ai.getText();
             String NI=ni.getText();
                 try {
                     if(dateDebut.length()!=0 && dateFin.length()!=0 && AI.length()!=0 && NI.length()!=0 && 
                             !jComboBox.getSelectedItem().toString().equals("")){
                         
                        cpt= jComboBox.getSelectedItem().toString();
                        Connection conn;
                        conn = ConnectBD.BD();
                        //Enregistrement d'une consommationn
                        Statement state=conn.createStatement();
                        ResultSet rst=state.executeQuery("SELECT idCompteur FROM compteur WHERE numCompteur='"+cpt+"'");
                        int idCpt=0;
                        while(rst.next())
                            idCpt=rst.getInt("idCompteur");
                        //Verifier si une comsommation a été déja enregistrée sur ce compteur
                        Statement st=conn.createStatement();
                        ResultSet rs=st.executeQuery("SELECT idCompteur FROM consommation");
                        Boolean existe=false;
                        while(rs.next()){
                                if(idCpt==rs.getInt("idCompteur"))
                                    existe=true;
                                    break;
                             }
                        if(existe){
                            Statement s=conn.createStatement();
                            ResultSet r=s.executeQuery("SELECT dateFin,NI From consommation WHERE idCompteur='"+idCpt+"'");
                            while(r.next()){
                                dateDebut=r.getString("dateFin");
                                AI=r.getString("NI");
                            }
                        }
                         
                        PreparedStatement ps=conn.prepareStatement("INSERT INTO consommation (dateDebut,dateFin,AI,"+
                            "NI,idCompteur) VALUES (?,?,?,?,?)");
                        //Verification du format de la date   
                         try {
                             ps.setDate(1, java.sql.Date.valueOf(dateDebut));
                             ps.setDate(2, java.sql.Date.valueOf(dateFin));
                         } catch (Exception e) {
                             JOptionPane.showMessageDialog(null, "La date doit être sous format aaaa-mm-jj");
                         }
                         //Verification du format des index entrés
                         try {
                             ps.setInt(3, Integer.parseInt(AI));
                             ps.setInt(4, Integer.parseInt(NI));
                         } catch (Exception e) {
                             JOptionPane.showMessageDialog(null, "l'index doit être sous format numérique");
                         }
                        ps.setInt(5, idCpt);
                        ps.executeUpdate();
                        conn.close();
                        //message
                        JOptionPane.showMessageDialog(null, "Consommation enregistrée avec succés");
                        msg.setText("Enregistrement réussie");
                        msg.setForeground(Color.green);
                        debut.setText("");
                        fin.setText("");
                        ai.setText("");
                        ni.setText("");
                        }else{
                            errorMessage("Veuiller remplir les champs vides", msg);
                     }
                 } catch (Exception e) {
                     JOptionPane.showMessageDialog(null, "Veuiller indiquer le numéro de compteur");
                 }
                 
             
    }//GEN-LAST:event_jButton1ActionPerformed


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=identifiant.getText();
        String mdp=motdepasse.getText();
        if(id.length()!=0 && mdp.length()!=0){
            try {
                Connection conn=ConnectBD.BD();
                Statement state=conn.createStatement();
                ResultSet rs=state.executeQuery("SELECT * FROM admin");
                while(rs.next()){
                   if(rs.getString("identifiant").equals(id) && rs.getString("motdepasse").equals(mdp)){
                       this.setVisible(false);
                       new Dashboard().setVisible(true);
                   }else{
                       identifiant.setText("");
                       motdepasse.setText("");
                       msg.setText("l'identifiant ou le mot de passe est incorrect");
                       msg.setForeground(Color.red);
                   } 
                }
            } catch (SQLException ex) {
                Logger.getLogger(Authentification.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            msg.setText("Veuiller remplir les champs vides");
            msg.setForeground(Color.red);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

